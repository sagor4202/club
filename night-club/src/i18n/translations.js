export function translate(language, key, values = {}) {
  const template = TRANSLATIONS[language]?.[key] ?? EN[key] ?? key;
  return String(template).replace(/\{\{(\w+)\}\}/g, (_, name) =>
    Object.prototype.hasOwnProperty.call(values, name) ? values[name] : ""
  );
}

function normalizeText(value) {
  return String(value || "").replace(/\s+/g, " ").trim();
}

export function translateKnownText(language, source) {
  const normalizedSource = normalizeText(source);
  const key = Object.keys(EN).find((item) => normalizeText(EN[item]) === normalizedSource);
  if (!key) return null;
  return translate(language, key);
}

export function isKnownTranslatedText(source, language = null) {
  const normalizedSource = normalizeText(source);
  const languageCodes = language ? [language] : Object.keys(TRANSLATIONS);

  return languageCodes.some((code) =>
    Object.values(TRANSLATIONS[code] || {}).some(
      (value) => normalizeText(value) === normalizedSource
    )
  );
}

export function getLanguageLabel(language) {
  return LANGUAGES.find((item) => item.code === language)?.label ?? "English";
}
